#!/usr/bin/env node
// Copies poetic/browser/poetic.css into a generated TS module so app code
// can import it as a plain string, sidestepping Turbopack's inability to
// place a require.resolve()'d or `?raw`-imported CSS file from a
// node_modules package into the server/client module graph. Runs on every
// `npm install` (see package.json's postinstall) so it stays in sync
// whenever the pinned poetic version bumps.

import { createRequire } from "node:module";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const require = createRequire(import.meta.url);
const cssPath = require.resolve("poetic/browser/poetic.css");
let css = readFileSync(cssPath, "utf8");

// Override: make the poem title visible (issue #41).
// The original poetic.css hides `.poem-info .title` — remove that rule.
// This regex is matched against the exact formatting of the pinned poetic
// version; see TECH-DEBT.md TD26071802 for the upgrade-safety gap.
css = css.replace(
  /\.poem-info\s+\.title,\s*\.poem-info\s+#title\s*\{\s*display:\s*none;\s*\}/g,
  "",
);

const outPath = path.join(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
  "src",
  "lib",
  "poetic-css.generated.ts",
);

const contents = `// Generated by scripts/sync-poetic-css.mjs from poetic/browser/poetic.css.
// Do not edit by hand — re-run \`npm install\` to regenerate.
export const poeticCss = ${JSON.stringify(css)};
`;

writeFileSync(outPath, contents);
console.log(`Wrote ${outPath} (${css.length} bytes)`);
